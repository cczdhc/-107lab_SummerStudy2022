package com.itheima.create;

public class ArrayDemo3 {
    public static void main(String[] args) {
        int[] arr = new int[10];
        System.out.println(arr[0]);
        System.out.println(arr[9]);

        char[] chars = new char[100];
        System.out.println((int)chars[0]);
        System.out.println((int)chars[99]);

        double[] scores = new double[90];
        System.out.println(scores[0]);
        System.out.println(scores[80]);

        boolean[] booleans = new boolean[100];
        System.out.println(booleans[1]);
        System.out.println(booleans[10]);

        String[] ches = new String[100];
        System.out.println(ches[0]);
        System.out.println(ches[90]);
    }
}
